class Switch(object):
    """Class to handle switch statements"""

    def __init__(self, value):
        """Constructs object."""
        # value  argument to select match
        self.value = value
        self.fall = False

    def __iter__(self):
        """Return the match method once, then stop"""
        yield self.match

    def match(self, *args):
        """Indicate whether or not to enter a case suite"""
        if self.fall or not args:
            return True
        elif self.value in args:
            self.fall = True
            return True
        else:
            return False
